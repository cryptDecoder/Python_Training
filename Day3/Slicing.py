"""
    in slicing 
    [start : stop : step]
    
    start :  its a initial index like 0 
    stop : end of string (last point)
"""
s = "Mansi"
print(len(s))
print(s[0])
print(s[1:4])

print(s[0:len(s):2])

print(s[0::])
print(s[::3])
print(s[:3:])


a = [10,20,56,45,62]
